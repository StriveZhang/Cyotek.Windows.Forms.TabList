namespace Cyotek.Windows.Forms
{
  /// <summary>
  /// Defines values representing <see cref="TabList"/> events.
  /// </summary>
  public enum TabListAction
  {
    /// <summary>
    /// Represents the <see cref="TabList.Selecting"/> event.
    /// </summary>
    Selecting = 1,

    /// <summary>
    /// Represents the <see cref="TabList.Selected"/> event.
    /// </summary>
    Selected,

    /// <summary>
    /// Represents the <see cref="TabList.Deselecting"/> event.
    /// </summary>
    Deselecting,

    /// <summary>
    /// Represents the <see cref="TabList.Deselected"/> event.
    /// </summary>
    Deselected,
  }
}
